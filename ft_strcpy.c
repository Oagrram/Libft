#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*ret;

	if (dst == NULL || src == NULL)
		return (NULL);
	ret = dst;
	while (1)
	{
		*dst = *src;
		if (*dst == '\0')
			break ;
		++dst;
		++src;
	}
	return (ret);
}
