#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	size_t	len;
	char	*ret;

	if (s == NULL)
		return (NULL);
	len = 0;
	while (s[len])
		len++;
	ret = (char *)ft_malloc(len + 1);
	if (ret == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		ret[i] = s[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
